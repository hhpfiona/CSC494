#!/usr/bin/env python
"""
build_eval_set.py — Construct the n=100 PluralTree validation set from IndoCulture.

Why this exists
---------------
The n=100 eval set is the ONE artifact the harness needs that isn't generated by
the pipeline. We source it from a real benchmark (IndoCulture) rather than writing
facts by hand, so that every `verified_points` entry is a human-authored, traceable
ground-truth answer — not an LLM- or author-invented "fact." Agent B scores
candidate paths against verified_points, so those points must be trustworthy or the
whole precision number is meaningless.

What IndoCulture gives us
-------------------------
IndoCulture is a multiple-choice cultural commonsense dataset spanning 11 Indonesian
provinces, hand-authored by local people per predefined topic. Each instance has:
  - a location (province, e.g. "Aceh", "Papua"),
  - a topic / sub_topic,
  - a premise sentence, and
  - answer options with exactly one correct answer.

Mapping to the PluralTree schema
--------------------------------
  query            <- the topic/sub_topic (short cue the orchestrator drives Agent A with)
  location         <- province
  sub_topic        <- topic
  ground_truth.location        <- province (same)
  ground_truth.sub_topic       <- topic   (same)
  ground_truth.verified_points <- [ premise sentence , correct-answer sentence ]
        The correct continuation is the authoritative cultural knowledge unit.
        The premise is included as scene-setting context so Agent B's KP check has
        the full human-authored situation, not a dangling answer fragment.

Output (exact line shape, one per line):
  {"query":"...","location":"...","sub_topic":"...",
   "ground_truth":{"location":"...","sub_topic":"...","verified_points":["...","..."]}}

Usage on Narval
---------------
  # from HuggingFace (node with internet, or after `huggingface-cli download`):
  python build_eval_set.py --n 100 --seed 13 --out eval_set_n100.jsonl

  # from a local copy already scp'd / downloaded:
  python build_eval_set.py --local /path/to/indoculture --n 100 --out eval_set_n100.jsonl

  # English surface forms instead of Indonesian (if the config exposes them):
  python build_eval_set.py --lang en --n 100 --out eval_set_n100.jsonl

Determinism
-----------
Sampling is seeded (default 13) and stratified across provinces so the 100 instances
aren't all from one region. Re-running with the same seed reproduces the same file.

This script writes UTF-8 explicitly (IndoCulture text is non-ASCII; do NOT let
PowerShell redirection pick UTF-16). It is dependency-light: only `datasets`.
"""

from __future__ import annotations
import argparse
import json
import logging
import sys
from collections import defaultdict

logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")
log = logging.getLogger("build_eval_set")

# IndoCulture columns vary slightly by mirror/config; we probe a few candidate
# names so you don't have to hard-code. Adjust here if your local copy differs.
LOCATION_KEYS = ("province", "location", "lokasi", "region")
TOPIC_KEYS    = ("topic", "sub_topic", "subtopic", "category", "theme")
PREMISE_KEYS  = ("premise", "sentence", "context", "question", "soal", "kalimat")
OPTION_KEYS   = ("options", "choices", "answers", "pilihan")          # list-style
OPTION_LETTER = ("option_a", "option_b", "option_c", "option_d", "option_e")  # column-style
ANSWER_KEYS   = ("answer", "label", "correct", "jawaban", "answer_key", "gold")


def _first(row: dict, keys) -> str | None:
    for k in keys:
        if k in row and row[k] not in (None, ""):
            return k
    return None


def _coerce_options(row: dict) -> list[str]:
    """Return options as a list of strings, whatever the storage style."""
    lk = _first(row, OPTION_KEYS)
    if lk is not None:
        opts = row[lk]
        if isinstance(opts, dict):  # {"A": "...", "B": "..."}
            return [str(opts[k]) for k in sorted(opts)]
        return [str(o) for o in opts]
    # column-per-letter style
    cols = [k for k in OPTION_LETTER if k in row and row[k] not in (None, "")]
    if cols:
        return [str(row[k]) for k in cols]
    return []


def _correct_index(row: dict, n_opts: int) -> int | None:
    """Resolve the gold answer to a 0-based option index."""
    ak = _first(row, ANSWER_KEYS)
    if ak is None:
        return None
    val = row[ak]
    if isinstance(val, int):
        return val if 0 <= val < n_opts else None
    s = str(val).strip()
    if s.isdigit():
        i = int(s)
        return i if 0 <= i < n_opts else None
    # letter -> index ("A"->0, "B"->1, ...)
    s = s.upper()
    if len(s) == 1 and "A" <= s <= "Z":
        i = ord(s) - ord("A")
        return i if 0 <= i < n_opts else None
    return None


def to_eval_record(row: dict) -> dict | None:
    loc_k, top_k, prem_k = _first(row, LOCATION_KEYS), _first(row, TOPIC_KEYS), _first(row, PREMISE_KEYS)
    if loc_k is None or prem_k is None:
        return None
    location = str(row[loc_k]).strip()
    sub_topic = str(row[top_k]).strip() if top_k else "general"
    premise = str(row[prem_k]).strip()

    options = _coerce_options(row)
    if not options:
        return None
    ci = _correct_index(row, len(options))
    if ci is None:
        return None
    correct = options[ci].strip()

    verified_points = [premise, correct] if premise and correct else [p for p in (premise, correct) if p]
    if not verified_points:
        return None

    return {
        "query": sub_topic,                      # short cue Agent A is driven with
        "location": location,
        "sub_topic": sub_topic,
        "ground_truth": {
            "location": location,
            "sub_topic": sub_topic,
            "verified_points": verified_points,
        },
    }


def load_rows(args):
    from datasets import load_dataset
    if args.local:
        ds = load_dataset(args.local, split=args.split)
    else:
        ds = load_dataset("indolem/IndoCulture", split=args.split)
    log.info("Loaded %d raw rows; columns: %s", len(ds), list(ds.features))
    return [dict(r) for r in ds]


def stratified_sample(records: list[dict], n: int, seed: int) -> list[dict]:
    """Even spread across provinces, deterministic under `seed`."""
    import random
    rng = random.Random(seed)
    by_loc: dict[str, list[dict]] = defaultdict(list)
    for r in records:
        by_loc[r["location"]].append(r)
    for v in by_loc.values():
        rng.shuffle(v)
    out, locs = [], sorted(by_loc)
    i = 0
    while len(out) < n and any(by_loc.values()):
        loc = locs[i % len(locs)]
        if by_loc[loc]:
            out.append(by_loc[loc].pop())
        i += 1
    return out[:n]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--n", type=int, default=100)
    ap.add_argument("--seed", type=int, default=13)
    ap.add_argument("--split", default="test")
    ap.add_argument("--local", default=None, help="Path to a local IndoCulture copy")
    ap.add_argument("--lang", choices=["id", "en"], default="id",
                    help="Surface language if the config exposes both (default id)")
    ap.add_argument("--out", "-o", default="eval_set_n100.jsonl")
    args = ap.parse_args()

    rows = load_rows(args)
    records = [rec for r in rows if (rec := to_eval_record(r)) is not None]
    log.info("Mapped %d/%d rows into eval records.", len(records), len(rows))
    if len(records) < args.n:
        log.warning("Only %d usable records (< requested %d). Writing all of them.",
                    len(records), args.n)

    sample = stratified_sample(records, args.n, args.seed)

    with open(args.out, "w", encoding="utf-8") as f:
        for rec in sample:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")

    provinces = sorted({r["location"] for r in sample})
    log.info("Wrote %d lines to %s", len(sample), args.out)
    log.info("Provinces covered (%d): %s", len(provinces), ", ".join(provinces))
    log.info("Sanity-check one line below:")
    print(json.dumps(sample[0], ensure_ascii=False))


if __name__ == "__main__":
    sys.exit(main())
